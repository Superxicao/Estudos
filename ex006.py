n=float(input('Digite um número: '))

dobro=n*2
triplo=n*3
raiz=n**(1/2)

print('O número digitado foi: {}\nSeu dobro é: {}\nSeu triplo é: {}\nSua raiz quadrada é: {:.40f}'.format(n,dobro,triplo,raiz))

print('O número digitado foi: {}\nSeu dobro é: {}\nSeu triplo é: {}\nSua raiz quadrada é: {:.4f}'.format(n,(n*2),(n*3),(n**(1/2))))

